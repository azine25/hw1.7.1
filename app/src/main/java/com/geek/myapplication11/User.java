package com.geek.myapplication11;

public class User {
    private int num;
    private int color;

    public User(int num, int color) {
        this.num = num;
        this.color = color;
    }

    public int getColor() {
        return color;
    }

    public void setColor(int color) {
        this.color = color;
    }

    public int getNum() {
        return num;
    }

    public void setNum(int num) {
        this.num = num;
    }



}
